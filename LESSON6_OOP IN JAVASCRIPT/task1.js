/**Đề bài: Quản lý thông tin sản phẩm**

1. Tạo một chương trình để quản lý thông tin sản phẩm.

2. Khai báo lớp `Product` với các thuộc tính sau:
   - `name`: Tên sản phẩm.
   - `price`: Giá sản phẩm.
   - `quantity`: Số lượng sản phẩm.
   - `description`: Mô tả sản phẩm.
   - `category`: Danh mục sản phẩm.

3. Lớp `Product` có các phương thức sau:
   - `showInfo()`: Hiển thị thông tin về sản phẩm bao gồm tên, giá, số lượng, mô tả và danh mục.
   - `changePrice(newPrice)`: Thay đổi giá sản phẩm thành `newPrice`.
   - `changeDescription(newDescription)`: Thay đổi mô tả sản phẩm thành `newDescription`.
   - `changeCategory(newCategory)`: Thay đổi danh mục sản phẩm thành `newCategory`.

4. Tạo một đối tượng `Product1` từ lớp `Product` với các giá trị sau:
   - Tên: "iPhone"
   - Giá: 1000
   - Số lượng: 10
   - Mô tả: "Apple"
   - Danh mục: "Phone"

5. Hiển thị thông tin về sản phẩm:
   - Gọi phương thức `showInfo()` để hiển thị thông tin về sản phẩm.

6. Thay đổi giá, mô tả và danh mục của sản phẩm:
   - Gọi các phương thức `changePrice()`, `changeDescription()` và `changeCategory()` để thay đổi thông tin của sản phẩm.
   - In ra thông báo xác nhận thay đổi thành công.

7. Yêu cầu kỹ thuật:
   - Sử dụng JavaScript để tạo lớp `Product` và đối tượng `Product1`.
   - Sử dụng phương thức `showInfo()` để hiển thị thông tin sản phẩm.
   - Sử dụng các phương thức `changePrice()`, `changeDescription()` và `changeCategory()` để thay đổi thông tin sản phẩm.
   - In ra thông tin sản phẩm và kết quả xác nhận thay đổi thông tin trên console hoặc trình duyệt web. */